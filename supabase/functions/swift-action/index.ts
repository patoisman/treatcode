console.info('server started');

Deno.serve(async (req: Request) => {
  const site_url = Deno.env.get("SITE_URL");

  const data = {
    site_url: site_url,
  };

  return new Response(
    JSON.stringify(data),
    { headers: { 'Content-Type': 'application/json', 'Connection': 'keep-alive' }}
  );
});